def populate_rules():
    """Add rules to Google Sheets"""
    try:
        # Initialize service
        creds = Credentials.from_service_account_file(
            SHEETS_CREDENTIALS_PATH,
            scopes=['https://www.googleapis.com/auth/spreadsheets']
        )
        service = discovery.build('sheets', 'v4', credentials=creds)

        # Clear existing data
        service.spreadsheets().values().clear(
            spreadsheetId=SHEETS_ID,
            range="Правила!A:C"
        ).execute()

        # Add header
        header = [["Номер блока", "Название", "Текст"]]
        service.spreadsheets().values().update(
            spreadsheetId=SHEETS_ID,
            range="Правила!A1:C1",
            valueInputOption="RAW",
            body={"values": header}
        ).execute()

        # Add all rules
        service.spreadsheets().values().update(
            spreadsheetId=SHEETS_ID,
            range="Правила!A2:C5",
            valueInputOption="RAW",
            body={"values": RULES_DATA}
        ).execute()

        print("✅ Rules added successfully!")
        print("📋 Added 4 rule blocks to Google Sheets")
        return True

    except Exception as e:
        print(f"❌ Error: {e}")
        return False

if __name__ == "__main__":
    populate_rules()
